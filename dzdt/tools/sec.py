import base64


def encode_csv(input_file, output_file):
    with open(input_file, 'r', encoding='utf-8') as fin, \
         open(output_file, 'w', encoding='utf-8') as fout:
        for line in fin:
            encoded = base64.b64encode(line.strip().encode('utf-8')).decode('utf-8')
            fout.write(encoded + '\n')

def decode_csv(input_file, output_file):
    with open(input_file, 'r', encoding='utf-8') as fin, \
         open(output_file, 'w', encoding='utf-8') as fout:
        for line in fin:
            decoded = base64.b64decode(line.strip()).decode('utf-8')
            fout.write(decoded + '\n')


